data class Product(
    val title: String,
    val description: String,
    val price: Double,
    val rating: Float,
    val image: Int,  // imageUrl
    val imageList: List<Int> = emptyList(),
    val categoryId: Int,
    val category: String,
    val id: Int,
    val isFavorite: Boolean = false,
    val isOnSale: Boolean = false,
    val isAvailableNow: Boolean = true,
    val isNew: Boolean = false,
    val ingredients: List<String> = emptyList(),
    val createdAt : String,   // timestamp
    val updatedAt : String,   // timestamp
    val reviews: List<Review> = emptyList(),
)

class Category(
    val title: String,
    val image: Int,
    val id: Int,
)

data class Review(
    val title: String,
    val description: String,
    val rating: Float,
    val image: Int,
    val id: Int,
)

